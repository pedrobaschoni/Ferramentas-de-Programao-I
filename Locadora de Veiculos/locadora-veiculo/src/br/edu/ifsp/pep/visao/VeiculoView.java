    private void atualizarTabela() {
        
        if (veiculos.isEmpty()) {
            Mensagem.atencao("Não existem Veículos cadastrados.");
            return;
        }
        
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        modelo.setNumRows(0);
        for (Veiculo veiculo : veiculos) {
            modelo.addRow(new Object[]{veiculo.getModelo(), veiculo.getPlaca(), veiculo.isLocado() ? "Sim" : "Não", veiculo.getTipo().getNome(), veiculo.getTipo().getValorDiaria()});
        }
    }

