    private void bGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bGravarActionPerformed

        //Validar os campos de entrada
        String modelo = tfModelo.getText();
        if (modelo.isEmpty()) {
            Mensagem.atencao("Informe o Modelo.");
            tfModelo.requestFocus();
            return;
        }

        Object valuePlaca = ftfPlaca.getValue();
        if (valuePlaca == null) {
            Mensagem.atencao("Informe a Placa.");
            ftfPlaca.requestFocus();
            return;
        }
        Object valueAno = ftfAno.getValue();
        if (valueAno == null) {
            Mensagem.atencao("Informe o Ano.");
            ftfAno.requestFocus();
            return;

        }
        int ano = ((Long) ftfAno.getValue()).intValue();
        if (ano < 1900 || ano > LocalDate.now().getYear()) {
            Mensagem.atencao("Informe um Ano válido.");
            ftfAno.requestFocus();
            return;
        }

        String cidade = tfCidade.getText();
        if (cidade.isEmpty()) {
            Mensagem.atencao("Informe a Cidade.");
            tfCidade.requestFocus();
            return;
        }
        if (cbTipo.getSelectedIndex() < 0) {
            Mensagem.atencao("Informe o Tipo do Veículo.");
            cbTipo.requestFocus();
            return;
        }
        TipoVeiculo tipo = (TipoVeiculo) cbTipo.getSelectedItem();
        Veiculo veiculo = new Veiculo(valuePlaca.toString(), cidade, modelo, ano, tipo);

        try {
            veiculoDAO.inserir(veiculo);
            Mensagem.sucesso("Veículo cadastrado.");

            this.limparCampos();

            paineis.setEnabled(true);
            paineis.setSelectedIndex(0);
        } catch (Exception ex) {
            Mensagem.erro(ex.getMessage());
        }

    }//GEN-LAST:event_bGravarActionPerformed

    private void limparCampos() {
        tfModelo.setText("");
        tfCidade.setText("");
        ftfAno.setValue(null);
        ftfPlaca.setValue(null);

        cbTipo.setSelectedIndex(-1);

    }

